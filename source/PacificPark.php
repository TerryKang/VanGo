		<div id="placeInfo">
		<p>Pacific Spirit Regional Park is a 874 hectares (2,160 acres) park located in the University Endowment Lands, on Point Grey to the west of the city of Vancouver, British Columbia. It surrounds the endowment lands of the University of British Columbia on the shores of Georgia Strait in the Pacific Ocean. It is a nature preserve of the British Columbia government and classified under Electoral Area A.</p>
		<p>The park contains over 73 km of walking/hiking trails, 50 km of which are designated multi-use and available for cycling and horseback riding as well. There is a Park Centre which is located on W 16th Avenue.</p>
		<p>In 1975, BC Parks established ninety hectares of Pacific Spirit Regional Park as the UBC Endowment Lands Ecologial Reserve. This area is designated for forest research, and is not open to the public.</p>
