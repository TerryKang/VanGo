		<p>The entire section of beach in Pacific Spirit (running from Acadia Beach in the north to Trail #7 in the south), including Wreck Beach, is designated as clothing optional.</p>
		<p>There are free parking lots at Acadia, Spanish Bank and Jericho beaches, and at the information center on 16th Avenue. Pay parking can be found in a lot near Gate 7 to UBC (see map), at the UBC Museum of Anthropology, by the UBC Botanical gardens and in various visitor lots around UBC. Additionally, parking is available at different places along 16th Avenue, on SW Marine Drive from Gate 8 to the Museum of Anthropology, and along Old Marine Drive. Many of the UBC buses stop along the trails.</p>
		<p>There are washrooms at the head of Trail #7, Acadia Beach and at the head and base of Trail #6. The washrooms and picnic table at the Park Centre on 16th Avenue are wheelchair-accessible. Other accessible facilities include Cleveland, Heron and Imperial Trails, as well as the Clinton Meadow picnic area. However, trails are subject to use and weathering which may degrade them to the point where their original accessible design has been compromised. It is advisable to take an able-bodied person with you.</p>
		<p>Pacific Spirit Park Society is actively involved in Pacific Spirit Regional Park. For more information or to get involved with this association, contact the Community Development Coordinator at the West Area Parks Office.</p>
