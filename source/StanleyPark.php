		<p>Stanley Park is a 1,001-acre public park that borders the downtown of Vancouver, Canada and is almost entirely surrounded by waters of Vancouver Harbour and English Bay.</p>
		<p>he park has a long history and was one of the first areas to be explored in the city. The land was originally used by indigenous peoples for thousands of years before British Columbia was colonized by the British during the 1858 Fraser Canyon Gold Rush. For many years after colonization, the future park with its abundant resources would also be home to nonaboriginal settlers. The land was later turned into Vancouver's first park when the city incorporated in 1886. It was named after Lord Stanley, a British politician who had recently been appointed governor general.</p>
		<p>Unlike other large urban parks, Stanley Park is not the creation of a landscape architect, but rather the evolution of a forest and urban space over many years. Most of the manmade structures we see today were built between 1911 and 1937 under the influence of then superintendent W.S. Rawlings. Additional attractions, such as a polar bear exhibit, aquarium, and miniature train, were added in the post-war period.</p>
