		<h1>Getting to Queen Elizabeth Park</h1>
		<p>Queen Elizabeth Park is located at the geographic centre of Vancouver, at the junction of Cambie Street and West 33rd Avenue.</p>
		<p>There are entrances on several sides of the park, including Ontario Street and West 33rd Avenue, or along West 37th Avenue between Columbia and Mackie streets.</p>
		<p>Whether you want to explore the gardens, catch amazing city views, or play tennis or pitch & putt, the park is easy to access and get around in, whatever mode of transportation you choose.</p>
		
		
		<h2>Quarry Gardens</h2>
		<div id="Quarry_Garden">
			<img src="images/Quarry_Garden.jpg" alt="Thumbnail for Stock place">
		</div>
		<p>The main Quarry Garden is just west of the Bloedel Conservatory and offers a moment of high drama when first approached and viewed from the park's rim.</p>
		<p>The once massive excavation is now home to specimen trees, shrubs, perennials, bulbs and annuals selected for their foliage, form and flower. A stream and cascading waterfall add cool comfort and variety to the artfully contrived landscape.</p>
		<p>Located below and adjacent to Seasons in the Park restaurant is the smaller or North Quarry Garden. In contrast to the large quarry, this is a "dry" garden and has many oriental horticultural influences including an arching bridge over a stony streambed.</p>
		
		
		<h2>Arboretum</h2>
		<div id="Arboretum">
			<img src="images/Arboretum.jpg" alt="Thumbnail for Stock place">
		</div>
		<p>Canada's first civic arboretum was created along the park's north and north-western sections. The first plantings were done in 1949 on the north slopes by a group of Junior Forest Wardens. </p>
		<p>They began by planting blocks of timber species such as ponderosa pine, subalpine spruce, and Douglas fir. Most of the larger trees are about 60 years old. Among these is the coast redwood, which can grow to be one of the tallest trees in the world.</p>
		<p>There are now about 1500 trees from across Canada, along with many exotic species, creating a beautiful landscape.</p>
		
		
		<h2>Rose Garden</h2>
		<div id="Rose Garden">
			<img src="images/RoseGarden.jpg" alt="Thumbnail for Stock place">
		</div>
		<p>Located on the south-western perimeter of the park, the Rose Garden was built in 1967 to commemorate Canada’s Centennial. It contains many rose varieties including hardy hybrids such as the Parkland and Explorer series developed in Saskatchewan.</p>
		
		
