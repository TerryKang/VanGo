		<div id="placeInfo">
		<p>Queen Elizabeth Park, Vancouver’s horticultural jewel, is a major draw for floral display enthusiasts and view-seekers, and as a popular backdrop for wedding photos. At 152 metres above sea level, it's the highest point in Vancouver and makes for spectacular views of the park, city, and mountains on the North Shore.</p>
		<p>The 52-hectare park is home to the stunning Bloedel Conservatory. There is also a gorgeously landscaped quarry garden, the arboretum with its collection of exotic and native trees, sculptures including one by internationally renowned artist Henry Moore, and diverse recreational offerings such as tennis, lawn bowling and pitch & putt. The park is also the perfect setting for fine dining at Seasons in the Park, a picnic or stargazing!</p>
		
