		<p>In 1910, the McTavish brothers donated 5 hectare of canyon land to the District of North Vancouver.[2] They hoped a park would attract people to their real estate development; the District of North Vancouver accepted the gift and added another 4 hectares. Walter Draycott visited the Lynn Valey on a picnic in 1911 and fell in love with the rugged wilderness. In 1912, he bought 3 lots near the canyon for $600.</p>
		<p>Designs for the Lynn Canyon Suspension Bridge were created by civil engineer and architect, C.H. Vogel. The construction of the bridge was completed in 1911. Lynn Canyon Park and the suspension bridge were officially opened at the first Lynn Valley Days celebration on September 14, 1912.</p>
		<p>As a private operation the Suspension Bridge cost 10 cents per person to cross. Later the fee was reduced to 5 cents, but the bridge fell into disrepair and was finally closed. The District of North Vancouver made repairs to the bridge and reopened it, free to everyone. As of 2014, Lynn Canyon Park is 617 acres, making it the largest park in North Vancouver.</p>
