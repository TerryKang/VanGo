		<div id="placeInfo">
		<p>Lynn Canyon Park is a municipal park in the District of North Vancouver, British Columbia. When the park officially opened in 1912 it was only 12 acres (4.9 ha) in size, but it now encompasses 617 acres (250 ha). The park has many hiking trails of varying length and difficulty. The Baden-Powell Trail passes through the park crossing over the Lynn Canyon Suspension Bridge. Due to its natural landscape many TV series such as Stargate SG-1 and Stargate Atlantis used the area for filming.</p>
		<h1>History</h1>
		<p>Lynn Creek and Lynn Valley area are named after sapper John Linn, a Royal British Engineer who was granted land at the mouth of the creek in 1871. The Linn family name was often misspelled "Lynn". By the turn of the century Linn Creek had become Lynn Creek.</p>
